package chapter_02;

public class Variables {
	
	public static void main(String[] args) {
		
		int x;
		x = 10;
		
		int y = 20;
		
		int z = 30;
		
		System.out.printf("X = %d\n",x);
		System.out.printf("Y = %d\n",y);
		System.out.printf("Z = %d\n",z);
		
		System.out.printf("X+Y+Z = %d\n",x+y+z);
		
		System.out.println("\"나는 생각한다고로 존재한다\"");
		
		
		
		
	}

} 
